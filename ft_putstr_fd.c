#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	x;

	x = 0;
	if (fd > 0)
	{
		while (s[x] != '\0')
		{
			write(fd, &s[x], 1);
			x++;
		}
	}
}
